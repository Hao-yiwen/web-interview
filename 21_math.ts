// ------------------------------------------
// 1. Pow(x, n) [中等]
// ------------------------------------------
// 实现 pow(x, n)，即计算 x 的整数 n 次幂函数。
//
// 示例 1：
//   输入：x = 2.00000, n = 10
//   输出：1024.00000
//
// 示例 2：
//   输入：x = 2.10000, n = 3
//   输出：9.26100
//
// 示例 3：
//   输入：x = 2.00000, n = -2
//   输出：0.25000（2^-2 = 1/2^2 = 1/4 = 0.25）

function myPow(x: number, n: number): number {
  if (n < 0) { x = 1 / x; n = -n; }
  let res = 1;
  while (n > 0) {
    if (n & 1) res *= x;
    x *= x; n >>= 1;
  }
  return res;
}
